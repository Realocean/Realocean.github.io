function initClick() {
    $(".delBatch").click(function(){
        var ids = vm.selectedRows();
        if(ids == null){
            return;
        }
        vm.del(ids);
    });
}

function initTable(table, soulTable) {
    table.render({
        id: "gridid",
        elem: '#grid',
        url: baseURL + 'customer/customerbalancerecord/queryList',
        where: JSON.parse(JSON.stringify(vm.q)),
        cols: [[
            {field:'amount', title: '记录金额', minWidth:200, templet: function (d) {return toMoney(d.amount);}},
            {field:'amountBefore', title: '记录前金额', minWidth:200, templet: function (d) {return toMoney(d.amountBefore);}},
            {field:'amountAfter', title: '记录后金额', minWidth:200, templet: function (d) {return toMoney(d.amountAfter);}},
            {field:'recordType', title: '记录类型', minWidth:200, templet: function (d) {return transformTypeByMap(d.recordType, {1:'增加', 2:'抵扣'});}},
            {field:'remark', title: '备注', minWidth:200, templet: function (d) {return isEmpty(d.remark);}},
            {field:'operationName', title: '操作人', minWidth:200, templet: function (d) {return isEmpty(d.operationName);}},
            {field:'timeCreate', title: '记录时间', minWidth:200, templet: function (d) {return dateFormat(d.timeCreate);}},
        ]],
        page: true,
        loading: true,
        limits: [20, 50, 100, 200],
        limit: 20,
        autoColumnWidth: {
            init: false
        },
        done: function(res, curr, count){
            soulTable.render(this);
            $('div[lay-id="gridid"]>div[class="layui-table-box"]>div>table').addClass('table-empty-left');
            $(".layui-table-main tr").each(function (index, val) {
                $($(".layui-table-fixed-l .layui-table-body tbody tr")[index]).height($(val).height());
                $($(".layui-table-fixed-r .layui-table-body tbody tr")[index]).height($(val).height());
            });
        }
    });

    initTableEvent(table);
    initTableEditListner(table);
}

